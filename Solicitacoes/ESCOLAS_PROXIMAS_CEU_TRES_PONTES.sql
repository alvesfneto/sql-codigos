
/*
Solicito a gentileza de informação de todas as escolas públicas municipais que se encontram no 
raio de até 4km do endereço Rua Capachós, 400, DRE MP, o qual funciona o CEU Três Pontes.
*/


DROP TABLE ##ESCOLAS_PROXIMAS_CEU_TRES_PONTES
SELECT 
DRE,
CD_ESCOLA,
SG_TP_ESCOLA,
NOMESC,
ENDERECO,
NR,
COMPL,
BAIRRO,
DISTR,
--CD_COORDENADA_GEO_Y,
--CD_COORDENADA_GEO_X,
'RUA CAPACHOS, 400' AS CEU_TRES_PONTES,
FLOOR((SQRT(POWER(CD_COORDENADA_GEO_Y *1000000 - (-23.480320*1000000),2)  + POWER(CD_COORDENADA_GEO_X *1000000 - (-46.380133*1000000),2))/10)/0.6) AS DISTANCIA,
CONVERT(VARCHAR, CRDATE-1, 103) AS DT_BASE
INTO ##ESCOLAS_PROXIMAS_CEU_TRES_PONTES
FROM SME_ESCOLA_DIARIO
WHERE CD_SIT = 1 AND TP_ESCOLA IN(1,2,3,4,10,16,17,18,28,30,31)
AND FLOOR((SQRT(POWER(CD_COORDENADA_GEO_Y *1000000 - (-23.480320*1000000),2)  + POWER(CD_COORDENADA_GEO_X *1000000 - (-46.380133*1000000),2))/10)/0.6)<=4000
AND CD_ESCOLA NOT IN (019367,019359,400676)  -- UNIDADES DO CEU TRES PONTES
