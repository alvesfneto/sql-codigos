
/*
Considerando que será entregue um novo prédio para instalação de um CEI Indireto, 
solicitamos o arquivo ponto a ponto do endereço das crianças das creches: 
CR.P.CONV. Jesus Menino - 306514 e 
CR.P.CONV. Irmã Jacinta - 309593 
para o endereço:

CEI Emanoel Alves de Araújo
Rua Porto dos Gaúchos n.º 181 ---23.591936472537256, -46.53927624553666
Vila Ema
Distrito/Setor Parque São Lucas
*/

DROP TABLE ##ALUNOS_CEIS_306514_309593_PARA_R_PORTO_DOS_GAUCHOS_181
SELECT DISTINCT
DRE,
B.CD_ESCOLA,
B.SG_TP_ESCOLA,
NOMESC,
SG_ETAPA,
A.SG_SERIE_ENSINO,
COD_ALUNO,
NM_ALUNO,
DATEDIFF(YY,A.DT_NASCIMENTO_ALUNO,GETDATE()) AS IDADE,
concat(C.TP_LOGRADOURO,' ',C.ENDEREÇO,', ',C.NR,' ',C.COMPL,' ',C.BAIRRO) AS END_ALUNO,
C.DIST_ESCOLA,
'Rua Porto dos Gaúchos n.º 181'AS DESTINO, 
FLOOR(FLOOR(SQRT(POWER((C.LAT_ALUNO * 1000000 - (-23.591936472537256 * 1000000) ),2) +  
				 POWER((C.LON_ALUNO * 1000000 - (-46.53927624553666 * 1000000)),2)) / 10)/0.6) AS DIST_DESTINO,
CONVERT(VARCHAR,A.CRDATE-1,103) AS DT_BASE

INTO ##ALUNOS_CEIS_306514_309593_PARA_R_PORTO_DOS_GAUCHOS_181

 FROM SME_ALUNOS_DIARIO A
JOIN SME_ESCOLA_DIARIO B ON A.CD_ESCOLA=B.CD_ESCOLA
JOIN VW_ALUNOS_ENDERECOS C ON A.CD_ALUNO=C.COD_ALUNO
WHERE 
C.CD_ESCOLA IN (306514,309593)

