
/*
Solicitamos uma planilha de alunos na faixa etária de infantil, com nascimento de 01/04/2017 a 31/03/2018, 
contendo, nome completo do aluno, código EOL do aluno, endereço completo e a escola de origem do alun
*/
DROP TABLE ##ALUNOS_BT_DTNASC_01042017A31032018
SELECT
B.DRE,
B.CD_ESCOLA,
B.SG_TP_ESCOLA,
B.NOMESC,
A.SG_ETAPA,
A.SG_SERIE_ENSINO,
A.NM_ALUNO,
--A.DT_NASCIMENTO_ALUNO,
CONVERT(VARCHAR,A.DT_NASCIMENTO_ALUNO,103) AS DT_NASCIMENTO,
A.CD_ALUNO,
C.ENDEREÇO,
C.NR,
C.COMPL,
C.BAIRRO
INTO ##ALUNOS_BT_DTNASC_01042017A31032018
FROM SME_ALUNOS A
JOIN SME_ESCOLA B ON A.CD_ESCOLA=B.CD_ESCOLA
JOIN VW_ALUNOS_ENDERECOS C ON A.CD_ALUNO=C.COD_ALUNO
WHERE A.DT_NASCIMENTO_ALUNO BETWEEN '2017-04-01' AND '2018-03-31'
AND DRE = 'DRE - BT' AND TP_ESCOLA<>15
