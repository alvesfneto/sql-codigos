DROP TABLE ##ESCOLAS_DISTANTES_1500M_ALUNOS_EOL309212
SELECT 
A.DRE,A.CD_ESCOLA,A.SG_TP_ESCOLA,NOME_ESCOLA,ETAPA,SERIE_ANO,NOME_ALUNO, 
CONCAT(B.CD_ESCOLA,' ',B.SG_TP_ESCOLA,'',NOMESC) AS DESTINO,
DIST_ALU_DESTINO= FLOOR(FLOOR(SQRT(POWER((LAT_ALUNO * 1000000 - (CD_COORDENADA_GEO_Y * 1000000) ),2) +  
				 POWER((LON_ALUNO * 1000000 - (CD_COORDENADA_GEO_X * 1000000)),2)) / 10)/0.6),
A.CRDATE
INTO ##ESCOLAS_DISTANTES_1500M_ALUNOS_EOL309212
FROM

VW_ENDERECOS_ALUNOS_COM_RESPONSAVEIS A, SME_ESCOLA_DIARIO B
              
WHERE A.CD_ESCOLA= 309212 AND B.TP_ESCOLA IN (10,11,12,18,28,31) AND CD_SIT=1
AND FLOOR(FLOOR(SQRT(POWER((LAT_ALUNO * 1000000 - (CD_COORDENADA_GEO_Y * 1000000) ),2) +  
				 POWER((LON_ALUNO * 1000000 - (CD_COORDENADA_GEO_X * 1000000)),2)) / 10)/0.6)<=1500
