DROP TABLE ##ALUNOS_DISTANTES_1500_M_CEI_RESERVA_RAPOSO_I
SELECT DISTINCT
A.DRE,
A.CD_ESCOLA,
A.SG_TP_ESCOLA,
A.NOME_ESCOLA,
A.ETAPA,
A.SERIE_ANO,
A.COD_ALUNO,
A.NOME_ALUNO,
A.MÃE_ALUNO,
A.RESPONSÁVEL,
CONCAT(TP_LOGRADOURO,'',ENDEREÇO,',',NR,COMPL,BAIRRO) AS END_ALUNO,
A.DDD,
A.TEL_CELULAR,
A.TEL_FIXO,
A.EMAIL_RESPONSAVEL,
CONVERT(VARCHAR,B.DT_NASCIMENTO_ALUNO,103) AS DT_NASC,
FLOOR(FLOOR(SQRT(POWER((LAT_ALUNO * 1000000 - (-23.583069 * 1000000) ),2) +  
		   POWER((LON_ALUNO * 1000000 - (-46.794159 * 1000000)),2))/10)/0.6) AS DISTANCIA,
A.CRDATE

INTO ##ALUNOS_DISTANTES_1500_M_CEI_RESERVA_RAPOSO_I
FROM VW_ENDERECOS_ALUNOS_COM_RESPONSAVEIS A
JOIN SME_ALUNOS_DIARIO B ON A.COD_ALUNO=B.CD_ALUNO
WHERE DT_NASCIMENTO_ALUNO BETWEEN '2017-04-01' AND '2023-01-01'
AND FLOOR(SQRT(POWER((LAT_ALUNO * 1000000 - (-23.583069 * 1000000) ),2) +  
				 POWER((LON_ALUNO * 1000000 - (-46.794159 * 1000000)),2)) / 10)/0.6<=1500
AND ETAPA='ED INF'


