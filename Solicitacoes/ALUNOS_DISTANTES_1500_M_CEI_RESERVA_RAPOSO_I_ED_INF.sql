que residam até 1,5 km da Rua Cândido Fontoura, nº 957 (Distrito /setor Raposo Tavares 02).

    A planilha precisa conter nome do aluno, filiação, data de nascimento,  endereço completo, contato, km do seu endereço até o CEI RESERVA RAPOSO I (Rua Cândido Fontoura, nº 957).


